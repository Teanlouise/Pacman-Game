package pacman.hunter;

/**
 * Definition of HunterType's available in the game.
 *      "SPEEDY" - Can run temporarily
 *      "PHASEY" - Can phase through ghosts temporarily.
 *      "HUNGRY" - Can eat ghosts temporarily.
 *      "PHIL" - basic phil, has no special ability.
 */
public enum HunterType {
    SPEEDY,
    PHASEY,
    HUNGRY,
    PHIL
}

